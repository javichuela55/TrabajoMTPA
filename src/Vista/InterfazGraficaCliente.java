    private void pulsarBoton(javax.swing.JButton boton){
        cambiarColor(boton, Color.red);
        
        
        
        cliente.jugadaRealizada(TipoJugada.valueOf(boton.getText().toUpperCase()),partida);
        timer.stop();
        
        
        resetearBotones();
        resetearTiempo();
        startTiempo();
    }
    public void cambiarColor(javax.swing.JButton boton, Color color){
        boton.setBackground(color);
        
    }
    public void resetearBotones(){        
        btnPiedra1.setBackground(Color.lightGray);
        btnPapel1.setBackground(Color.lightGray);
        btnTijera1.setBackground(Color.lightGray);
    }
    
    public void resetearTiempo(){
        contador=5;
       
    }
    
    public void startTiempo(){
        timer.start();
    }
    
